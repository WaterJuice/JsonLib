#define JlLinkedListAddToEnd( HEAD, TAIL, NEWITEM ) \
{                                                   \
    if( NULL != (TAIL) )                            \
    {                                               \
        (TAIL)->Next = (NEWITEM);                   \
    }                                               \
    else                                            \
    {                                               \
        (HEAD) = (NEWITEM);                         \
    }                                               \
    (TAIL) = (NEWITEM);                             \
}
